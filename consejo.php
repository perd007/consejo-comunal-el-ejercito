      <p align="justify">PERFIL DE LA  COMUNIDAD EL EJÉRCITO.</p>
      <p align="justify"><strong>-UBICACIÓN:</strong> Av. El Ejército a 500 Mts  aproximadamente del Aeropuerto Cacique Aramare.</p>
      <p align="justify"><br />
        -<strong> RESEÑA  HISTÓRICA DE LA COMUNIDAD: </strong>En los años  1930 llegan al Territorio Federal Amazonas ¨ Las Misiones Salesianas&rdquo; (Los  Curas ) los cuales el Gobierno Nacional vende mediante documentación, un lote  de terreno aproximadamente  de un millón  de hectáreas (1.000.000 has) en las adyacencia de la Capital Puerto Ayacucho,  lo que se conoció como <strong>&ldquo; La Granja de  los Curas&rdquo;,</strong> donde se realizaban actividades educativas como agricultura y  donde todavía  se pueden observar trazos  de construcción y de  aljibes de esa  época. Este pedazo de terreno iba desde los límites: del Aeropuerto local, San  Enrique, Chaparralito hasta la urbanización Simón Bolívar de esta Ciudad. </p>
      <p align="justify"><br />
        A medida que la ciudad se  desarrollaba y poblaba se les redujo su posesión por lo cual en el año de 1980  aproximadamente, el señor Obispo, Monseñor <strong>ENZO  SCHACARELLI</strong>, empieza  a vender lotes  de terrenos mediante la Sociedad Pedagógica Salesianas radicada en la Ciudad de  Caracas.</p>
      <p align="justify">Alrededor de esa época llegan a  Puerto Ayacucho tres mujeres procedentes de Caracas y de origen Andino (San  Pedro del Rio) las cuales fueron la Sra. Victoria de Roa, ama de casa, Hilda  Roa Suarez, Abogada y Socióloga, y Elvighia E. Roa Suarez, Ingeniero Civil y  Licenciada en Administración. La Ciudadana Hilda M. Roa Suarez con gran visión  de futuro, el referido monseñor hace contactó con ella y le proponen vender un  lote de terreno aproximadamente de 200 has, pero sus condiciones económicas no  le permiten adquirir la extensión propuesta y cierran el negocio por 130has.  Aprox., es de hacer notar que esos terrenos estaban formadas por sabanas  improductivas, chaparrales y morichales, donde tenían su habita  conejos, venados, morrocoyas y aves  silvestres. Para darle  validez a esta  venta lo protocolizan y Registran en la Oficina de Registro Público de Puerto  Ayacucho donde reposa esta documentación.</p>
      <p align="justify">La Señora Hilda María Roa, empezó a  desarrollar estos terrenos y construye una churuata y otras  edificaciones que está ubicada en las  cercanías de la avenida Orinoco vía aeropuerto   con la finalidad de hacer un centro turístico y de recreación para la  ciudad en crecimiento. Posteriormente la Sra. Hilda consiente en la  necesidad  hotelera de Puerto Ayacucho  inicio la Construcción de un Hotel con capacidad para cien habitaciones en  forma de cruz pero la muerte la sorprende por un cáncer  de pulmón y fallece en la Ciudad de Mérida el  año 1997.</p>
      <p align="justify">La sobreviene su hermana Elvighia E.  Roa Suarez, ingeniero civil y Licenciada en Administración, la cual continúa  con la construcción del Hotel, y con gran sensibilidad social que las caracterizaron  y  consientes de su ciudadanía, ceden un  lote de sus terrenos para  la  construcción y continuación de la avenida Perimetral que posteriormente toma el  nombre de &ldquo;<strong>Avenida El Ejercito&rdquo; </strong>al  Ministerio de obras Públicas de la época.<br />
        En vista del desarrollo apresurado de Puerto Ayacucho  y la necesidad de construcción de vivienda   contrata los servicios de Topografía al señor Henry Rivas y se procede  al levantamiento topográfico de varios   lotes de terrenos de la venta original y los vende a precio solidarios,  a la vista se puede observar el desarrollo habitacional de los lotes,  beneficiando a cantidad  de familias venezolanos, luego ella fallece  en la Isla de Margarita en el año 2008.- <br />
      Se anexa plano original de los terrenos.</p>
      <p align="justify"><br />
        Puerto Ayacucho,  Septiembre de 2010<br />
