      <th scope="col"><p align="left"><strong>Beneficios del Proyecto</strong></p>
          <p align="left">Se refiere al bienestar que genera el proyecto.<strong> </strong>En esta sección se describen los  resultados esperados del proyecto, el impacto a corto, mediano y largo plazo,  hasta su influencia en la generación de trabajos y en mejora en la calidad de  vida de la comunidad. </p></th>
    </tr>
    <tr>
      <td><p><strong><u>Cuantitativo</u></strong></p>
          <ul>
            <li><strong>Producto  esperado:</strong> 30  viviendas totalmente  construidas, con todos los servicios básicos y preparados para ser habitadas. </li>
            <li><strong>Número de  personas beneficiadas: </strong>120 habitantes de la comunidad que se van a beneficiar  con la puesta en marcha del proyecto.</li>
            <li><strong>Número de  trabajo directo e indirecto: </strong> Con la  construcción de 30 viviendas se generarán 65 trabajos directos entre personal  obrero y técnico durante 30 días; <br />
              una vez, en funcionamiento se generarán 10  trabajos indirectos en el mantenimiento de las áreas verdes,  transporte público, aseo urbano, entre otros. </li>
          </ul></td>
    </tr>
    <tr>
      <td valign="top"><p><strong><u>Cualitativo</u></strong></p>
          <ul>
            <li><strong>Los  resultados esperados:</strong> es lo que se espera lograr a mediano plazo.</li>
          </ul>
        <p> 1.- Contribuir  a una mejor calidad de vida de los Habitantes de la Comunidad &ldquo;La Quebrada&rdquo;<br />
          2.-  Disminuir  el nivel  de epidemias<br />
          3.- Contribuir a un   desarrollo urbanístico comunal<br />
          4.- Incrementar   la producción y productividad de Rubros Agrícolas en la Comunidad de  mane<br />
          ra  sostenible y sustentable</p>
        <ul>
            <li><strong>Impacto: </strong>son los  efectos que va ha generar el proyecto en sus diferentes ámbitos (social,  económico, político, institucional, cultural , ambiental)</li>
          <li><strong>Área de Influencia:</strong> Todo el espacio Geográfico que  comprende la Poligonal  de la comunidad donde se realiza el proyecto permitiendo beneficios directos e  indirectos a los Habitantes de la misma.</li>
        </ul>
        <p><strong>&nbsp;</strong></p></td>
